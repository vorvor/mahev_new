<h3>GYAKORI KÉRDÉSEK AZ ÁLLAMI TÁMOGATÁSSAL KAPCSOLATOSAN</h3>

<h3>1. Milyen forrásból egészíthetjük ki a támogatást?</h3>

<p>- az autó maradék árának lehetséges fedezeti módjai:</p>

<p>- önerő</p>

<p>- zártvégű pénzügyi lízing vagy személyi kölcsön</p>

<h3>2. Milyen autót vásárolhatnak kedvezményesen a nagycsaládosok?</h3>

<p>Legalább hétszemélyes új személyautót.</p>

<h3>3. Mekkora támogatást igényelhetnek a nagycsaládosok az autó vásárlásához?</h3>

<p>2,5 millió forintot, de legfeljebb az autó bruttó vételára felének megfelelő támogatást.</p>

<h3>4. Kell-e bármit visszatéríteniük a nagycsaládosoknak?</h3>

<p>Nem, az igényelt összeg vissza nem térítendő támogatás.</p>

<h3>5. Mikor lehet igényelni a támogatást?</h3>

<p>2019. július 1-jétől.</p>

<h3>6. Meddig lehet igényelni a támogatást?</h3>

<p>2022. december 31-ig.</p>

<h3>7. Tovább értékesíthető a megvásárolt autó?</h3>

<p>A támogatás igénybevételével megszerzett személygépkocsi az adásvételi szerződés vagy a pénzügyi lízingszerződés hatálybalépésének napjától számított három éven belül nem idegeníthető el.</p>

<h3>8. Meg kell-e előlegeznie a támogatás összegét a vásárlónak?</h3>

<p>Nem, a támogatás összegét az állam közvetlenül a kereskedőnek utalja.</p>

<h3>9. Igényelhetik-e az egyszülős családok?</h3>

<p>IGEN.</p>

<h3>10. Milyen esetekben kell visszafizetni a támogatást (késedelmi kamattal együtt)?</h3>

<p>Ha az igénylési nyilatkozat a valóságnak <em>nem megfelelő adatokat</em> tartalmaz. Ha az igénylő nem teljesíti az elidegenítési tilalom bejegyeztetésére vonatkozó kötelezettségét, vagy ha <em>három éven belül házassága vagy élettársi kapcsolata úgy szűnik meg, hogy a gyermekek elhelyezése szempontjából már nem minősül nagycsaládosnak</em>. Akkor is vissza kell fizetni a támogatást, ha azt örökbefogadott gyermekre vonatkozóan állapították meg és a támogatás igénylésétől számított két éven belül felbontják az örökbefogadást.</p>

