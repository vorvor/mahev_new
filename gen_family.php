<p>2020 a fordulat éve ebben a témában, az idei évtől a Magyar Autókereskedőház Zrt. is tudja nyújtani a nagycsaládosok részére a Magyar Állam által biztosított 2,5 mHUF mértékű állami támogatást. Hogy mire lehet igénybe és ki jogosult ennek az igénylésére, pár kérdés/válaszban összefoglaltuk, íme:</p>

<h3>1. Mitől minősül a vásárló nagycsaládnak?</h3>

<p>Háztartás akkor minősül nagycsaládnak, ha legalább 3 gyermekünk van. (vagy több)</p>

<h3>2. Mitől minősül a vásárló nagycsaládosnak?</h3>

<p>(Mivel a törvény különbséget tesz nagycsalád és nagycsaládos fogalom között)</p>

<p>Nagycsaládosnak pedig az a személy (vásárló) minősül, aki három gyermeke után családi pótlékra jogosult</p>

<h3>3. Gyermeknek számít….?</h3>

<p>Gyermeknek számít meglévő babáink mellett a várandósság 12. hetét követően a magzat, valamint az igénylő háztartásában nevelt vagy gondozott tartósan beteg, súlyosan fogyatékos személy is. Ugyanakkor a családnál ideiglenesen elhelyezett GYERMEKET NEM VESZIK FIGYELEMBE a „nagycsaládos” státusz megállapításakor.&nbsp;Fontos, hogy csak meglévő gyermekekre, magzatokra jár a támogatást, gyerekvállalással nem lehetünk jogosulttá az „ingyen” pénzre.</p>

<p class="rtecenter"><img alt="" src="/resources/img/nagycsalados-kedvezmeny-1.jpg"></p>

<h3>4. Személyre vonatkozó feltételek:</h3>

<ul>
    <li>
    Az igénylőnek vagy házastársának B kategóriás jogosítvánnyal szükséges rendelkeznie, és nem tilthatták el a vezetéstől.
    </li>
    <li>
    A kérelmezőnek nem lehet NAV köztartozása.
    </li>
    <li>
    Az igénylő büntetlen előéletű kell legyen.
    </li>
    <li>
    Egyetlen alkalommal lehet igényelni az autóvásárlási támogatást.
    </li>
    <li>
    Magyar lakóhellyel vagy tartózkodási hellyel kell rendelkeznie a kérelmezőnek.
    </li>
    <li>
    az összes igényben feltüntetett személynek azonos lakóhellyel kell rendelkeznie
    </li>
</ul>

<h3>5. További feltételek, a támogatás visszafizetése</h3>

<h4><strong>Az&nbsp;autóra be kell jegyeztetnünk&nbsp;elidegenítési és terhelési tilalmat a vásárlást követő 3 évre.</strong></h4>

<ul>
    <li>A kereskedőnek pedig fel kell helyeznie az autóra a „Családbarát Magyarország” logót.&nbsp;A három év alatt a gépjármű nem értékesíthető, és az üzembentartói jog sem adható át másnak. Továbbá a jármű átalakítása, más kategóriában történő vizsgáztatása sem engedélyezett.</li>
    <li><strong>A támogatást vissza kell fizetni:</strong> Amennyiben utólagosan kiderül, hogy a személyi feltételek megállapításakor valótlanságot állítottunk.</li>
    <li>Ha az elidegenítési és terhelési tilalmat nem jegyeztettük be.</li>
    <li>Amennyiben házas-, élettársi kapcsolatunk három éven belül megszűnik, és ezt követően nem minősülünk nagycsaládosnak. (Például a válást követően két gyermeket nálunk, a harmadikat a másik félnél helyeznek el.)</li>
    <li>Örökbefogadott gyermekünkre vonatkozóan az örökbefogadási szerződést két éven belül felbontják.</li>
    <li>Az állami támogatás nem számít Önerőnek, az közvetlenül a jármű vételárát csökkenti!</li>
    <li>Amennyiben az autó eladásra kerül, vagy a lízingszerződést felmondják nemfizetés miatt három éven belül, akkor a támogatás időarányos, 3 évből még hátralévő részét szükséges visszafizetnünk.</li>
    <li>Ha a visszafizetendő pénz egyösszegű rendezése túl nagy terhet róna a családunkra, akkor a Kincstár kérelmünkre adhat 12 hónapos fizetési halasztást vagy 36 hónapos részletfizetést.</li>
</ul>


